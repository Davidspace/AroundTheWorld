// Definimos la clase y el constructor Alojamiento
function Alojamiento(nombre, tipo, descripcion, localizacion, coordenadas, valoracion){
  this.nombre = nombre;
  this.tipo = tipo;
  this.descripcion = descripcion;
  this.localizacion = localizacion;
  this.coordenadas = coordenadas;
  this.valoracion = valoracion;
}
