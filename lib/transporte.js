// Definimos la clase y el constructor Transporte
function Transporte(nombre, descripcion, tipo, localizacion, valoracion, hora_inicio, hora_fin){
  this.nombre = nombre;
  this.descripcion = descripcion;
  this.tipo = tipo;
  this.localizacion = localizacion;
  this.valoracion = valoracion;
  this.hora_inicio = hora_inicio;
  this.hora_fin = hora_fin
}
